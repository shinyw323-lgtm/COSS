/**
  * @brief This function handles DMA1 stream0 global interrupt.
  */
void DMA1_Stream0_IRQHandler(void)
{
  /* USER CODE BEGIN DMA1_Stream0_IRQn 0 */
    //전송 완료(Transfer Complete) 인터럽트 플래그(TCIF0)가 발생했는지 확인
    if (DMA1->LISR & DMA_LISR_TCIF0)
    {
        //인터럽트 플래그를 반드시 클리어해야 다음 인터럽트가 발생합니다.
        DMA1->LIFCR |= DMA_LIFCR_CTCIF0;
        //실제 데이터 처리 함수 호출
        ADC_Process_Data();
    }
  /* USER CODE END DMA1_Stream0_IRQn 0 */
  /* USER CODE BEGIN DMA1_Stream0_IRQn 1 */

  /* USER CODE END DMA1_Stream0_IRQn 1 */
}

/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
    // 1. 수신 인터럽트(RXNE)가 발생했는지 확인
	if (USART2->ISR & (1U << 5)) //RXNE bit
    {
        // RDR(Receive Data Register)를 읽으면 RXNE 플래그는 자동으로 클리어
        uint8_t received_data = (uint8_t)USART2->RDR;

        // 개행문자면 문자끝
        if (received_data == '\n' || received_data == '\r')
        {
        	uart_rx_buffer[uart_rx_index] = '\0'; // 문자열의 끝을 표시
        	command_received = true; // main 루프에 명령이 수신되었다고 알림
        	uart_rx_index = 0; // 다음 명령을 위해 인덱스 초기화
        }
        else // 개행 문자(\n 또는 \r)가 아니면 버퍼에저장
        {
        	uart_rx_buffer[uart_rx_index++] = received_data;
        }
    }
  /* USER CODE END USART2_IRQn 0 */
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}
